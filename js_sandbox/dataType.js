//primitive data type

//1.string
const name = 'kumar rahul';
console.log("Name = ",name);
console.log("typeOf name = "+typeof name);
console.log('==========');

//2.number
const age = 30;
console.log("Age = ",age);
console.log("typeof age = ",typeof age);
console.log('==========');

//3.boolean
const hasKids = true;
console.log("hasKids = ",hasKids);
console.log("typeof hasKids = ",typeof hasKids);
console.log('==========');

//4.null
const car= null;
console.log("car = ",car);
console.log("typeof car = ",typeof car);
console.log('==========');


//5.undefined
let test;
console.log("test = ",test);
console.log("typeof test = ",typeof test);
console.log('==========');

//6.symbol
const sym=Symbol();
console.log("sym = ",sym);
console.log("typeof sym = ",typeof sym);
console.log('==========');


